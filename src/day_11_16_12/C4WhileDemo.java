package day_11_16_12;

public class C4WhileDemo {
    public static void main(String[] args) {
        System.out.println("Using for loop");
        for(int i = 1; i<=5; i++) {
            System.out.println(i);
        }
        System.out.println("using while loop");
        //using while loop

        int j = 1;
        while(j<=5){

            System.out.println(j);
            j++;

        }
    }
}
